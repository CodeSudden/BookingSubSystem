function LearnMore() {
    document.getElementById("popupForm").style.display = "block";
    document.getElementById("backgroundForm").classList.add("blurred");
}

function closePopup() {
    document.getElementById("popupForm").style.display = "none";
    document.getElementById("backgroundForm").classList.remove("blurred");
}

